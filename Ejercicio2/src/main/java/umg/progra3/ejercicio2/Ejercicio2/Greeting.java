package umg.progra3.ejercicio2.Ejercicio2;

public class Greeting {
	private Long id;
	  private String content;
	  
	  public Greeting(Long id, String content) {
	    super();
	    this.id = id;
	    this.content = content;
	  }

	  public Long getId() {
	    return id;
	  }

	  

	  public String getContent() {
	    return content;
	  }

	  
	  
	  
	  
}
